    <h1>About Open Prompt</h1>
    <p class="subtitle">The source code of AI-built software.</p>
  </header>

  <section class="prose">
    <p>
      For decades, "open source" meant one thing: you could read the code. The code was the
      product, the artifact, the record of how something was built. To understand a piece of
      software, you read its source.
    </p>
    <p>
      That contract is breaking. The code is no longer where the decisions live. Software is
      increasingly generated by AI agents from human prompts — and the prompts are the real source.
      The code is the rendering.
    </p>
    <p>
      Open Prompt publishes the upstream layer. Every conversation that built a product — every
      ask, every correction, every "no, do it this way" — is the actual source code of the age
      we're in. View source still matters. The thing being viewed has just moved.
    </p>
  </section>

  <section class="prose">
    <h2>What you'll find here</h2>
    <div class="definition-list">
