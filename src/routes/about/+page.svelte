      </div>
      <div class="definition-row">
        <span>Conversations</span>
        <p>The actual exchanges — prompts, corrections, decisions — that shaped the product.</p>
      </div>
      <div class="definition-row">
        <span>Builders</span>
        <p>The humans behind the work, identified by their Nostr keys.</p>
      </div>
    </div>
  </section>

  <section class="prose">
    <h2>The technology</h2>
    <p>
      Everything on Open Prompt is built on Nostr. Conversations are signed events, portable
      across relays, censorship-resistant, and not tied to any one platform. No accounts to
      create — identities are cryptographic. Data lives on relays, not in our database.
    </p>
  </section>

  <section class="prose">
    <h2>The meta</h2>
    <p>
      Open Prompt is built using TENEX. The conversations that built this product are themselves
      browsable on Open Prompt. The medium is the message.
    </p>
  </section>
